package Trail;

public class CharAt {

	public static void main(String[] args) {
		String s1 = "Hello";
		char result1 = s1.charAt(0);// selects 1st element in the string
		System.out.println(result1);
		
		char result2 = s1.charAt(3);// selects 3rd element in the string
		System.out.println(result2);
	}

}
