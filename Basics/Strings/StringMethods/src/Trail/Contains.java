package Trail;

public class Contains {

	public static void main(String[] args) {
		String fullName = "Gowtham Kumar"; 
		System.out.println(fullName.contains("Gow"));//true
		System.out.println(fullName.contains("Hello"));//false
		System.out.println(fullName.contains("Kumar"));//true
		/*it will find out a string contains a sequence of characters
		 * and if matched it will give true
		 * if not matched then it will give false 
		 * */
	}

}
